def addition_of_num(num1 , num2):
    """
    @num1 : accept the integer
    @num2 : accept the integer
    """
    add = num1 + num2
    return add 

def substraction_of_num(num1 , num2):
    
    sub = num1 - num2
    return sub 

print(addition_of_num(6, 8))
print(substraction_of_num(10, 8))


